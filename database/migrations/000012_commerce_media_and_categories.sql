  'Unique R2 object key (path inside bucket). Deterministic; no user-supplied path segments.';

COMMENT ON COLUMN commerce.media_assets.bucket IS
  'R2 bucket name; default chicaboo-assets.';

COMMENT ON COLUMN commerce.media_assets.content_type IS
  'MIME type of the stored object.';

COMMENT ON COLUMN commerce.media_assets.byte_size IS
  'Object size in bytes; NULL until upload is complete.';

COMMENT ON COLUMN commerce.media_assets.width IS
  'Image width in pixels; NULL for non-image assets.';

COMMENT ON COLUMN commerce.media_assets.height IS
  'Image height in pixels; NULL for non-image assets.';

COMMENT ON COLUMN commerce.media_assets.checksum IS
  'Optional content hash (SHA-256 hex or R2 ETag) for deduplication and integrity.';

