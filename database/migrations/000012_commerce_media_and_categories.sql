-- =============================================================================
-- TABLE: commerce.categories
-- Unlimited-depth category tree via parent_id. path and depth are populated by
-- application or migration 000024 trigger. search_vector column exists but
-- GIN index is deferred to 000024.
-- =============================================================================
CREATE TABLE commerce.categories (
  -- Primary key (UUID v4 generated by PostgreSQL).
  id              UUID PRIMARY KEY DEFAULT gen_random_uuid(),

  -- Parent category; NULL for root nodes. ON DELETE RESTRICT prevents orphaning.
  parent_id       UUID REFERENCES commerce.categories (id) ON DELETE RESTRICT,

  -- Display name shown in navigation and admin.
  name            TEXT NOT NULL,

  -- URL-safe identifier; unique among non-deleted rows.
  slug            TEXT NOT NULL,

  -- Long-form category description for PLP header / SEO body.
  description     TEXT,

  -- Optional category hero image — R2 key referencing media_assets.r2_key pattern.
  image_r2_key    TEXT,

  -- Sibling sort order within the same parent (lower = first).
  sort_order      INTEGER NOT NULL DEFAULT 0,

  -- Publication state; inactive categories hidden from storefront.
  status          TEXT NOT NULL DEFAULT 'active',

  -- Materialised path e.g. /women/ethnic/sarees — maintained in 000024.
  path            TEXT,

  -- Depth in tree (0 = root); maintained in 000024.
  depth           INTEGER NOT NULL DEFAULT 0,

  -- SEO meta fields.
  seo_title       TEXT,
  seo_description TEXT,

  -- Full-text search vector; GIN index and refresh trigger deferred to 000024.
  search_vector   TSVECTOR,

  -- Extensible attributes without schema migration.
  metadata        JSONB NOT NULL DEFAULT '{}'::jsonb,

  -- Standard audit timestamps (UTC).
  created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  deleted_at      TIMESTAMPTZ,

  -- Allowed status values (CHECK instead of PostgreSQL ENUM).
  CONSTRAINT categories_status_check CHECK (
    status IN ('active', 'inactive')
  ),

  -- Tree depth and sort order must be non-negative.
  CONSTRAINT categories_depth_nonneg CHECK (depth >= 0),
  CONSTRAINT categories_sort_order_nonneg CHECK (sort_order >= 0)
);

