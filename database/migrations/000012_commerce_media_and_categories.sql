-- =============================================================================
-- TABLE: commerce.media_assets
-- Central registry for files stored in Cloudflare R2. Application code stores
-- object keys only — never presigned URLs (ADR-015, ARCHITECTURE_v3.md).
-- Immutable registry: no updated_at / deleted_at; supersede by inserting new row.
-- =============================================================================
CREATE TABLE commerce.media_assets (
  -- Primary key (UUID v4 generated by PostgreSQL).
  id              UUID PRIMARY KEY DEFAULT gen_random_uuid(),

  -- Unique object key within the bucket, e.g. products/{uuid}/main.webp.
  r2_key          TEXT NOT NULL,

  -- R2 bucket name; defaults to project bucket per tools.md.
  bucket          TEXT NOT NULL DEFAULT 'chicaboo-assets',

  -- MIME type, e.g. image/webp, application/pdf.
  content_type    TEXT,

  -- File size in bytes; NULL until upload finalised.
  byte_size       BIGINT,

  -- Image dimensions in pixels; NULL for non-image assets.
  width           INTEGER,
  height          INTEGER,

  -- Optional SHA-256 or ETag checksum for integrity verification.
  checksum        TEXT,

  -- UTC timestamp when the asset record was created.
  created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),

  -- Enforce non-negative size when byte_size is known.
  CONSTRAINT media_assets_byte_size_nonneg CHECK (
    byte_size IS NULL OR byte_size >= 0
  ),

  -- Enforce non-negative dimensions when provided.
  CONSTRAINT media_assets_width_nonneg CHECK (
    width IS NULL OR width >= 0
  ),
  CONSTRAINT media_assets_height_nonneg CHECK (
    height IS NULL OR height >= 0
  )
);

COMMENT ON TABLE commerce.media_assets IS
  'Registry of objects in Cloudflare R2. Stores r2_key only — never signed URLs.';

COMMENT ON COLUMN commerce.media_assets.id IS
  'UUID primary key.';

